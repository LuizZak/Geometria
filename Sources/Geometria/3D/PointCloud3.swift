/// A point-cloud composed of three-dimensional vector points.
public typealias PointCloud3<Vector: Vector3Type> = PointCloud<Vector>

/// A point-cloud composed of three-dimensional vector points of double-precision
/// floating-point type.
public typealias PointCloud3D = PointCloud3<Vector3D>

/// A point-cloud composed of three-dimensional vector points of single-precision
/// floating-point type.
public typealias PointCloud3F = PointCloud3<Vector3F>
