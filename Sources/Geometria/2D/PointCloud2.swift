/// A point-cloud composed of two-dimensional vector points.
public typealias PointCloud2<Vector: Vector2Type> = PointCloud<Vector>

/// A point-cloud composed of two-dimensional vector points of double-precision
/// floating-point type.
public typealias PointCloud2D = PointCloud2<Vector2D>

/// A point-cloud composed of two-dimensional vector points of single-precision
/// floating-point type.
public typealias PointCloud2F = PointCloud2<Vector2F>
